package com.opensource.maker.common;
public class Txareas{ 

		private int code;
	private String name;
	private int level;
	private int pcode;
	private String enCity;

	public int getCode(){
			return code; 
	}
	public void setCode(int code){
			this.code=code; 
	}

	public String getName(){
			return name; 
	}
	public void setName(String name){
			this.name=name; 
	}

	public int getLevel(){
			return level; 
	}
	public void setLevel(int level){
			this.level=level; 
	}

	public int getPcode(){
			return pcode; 
	}
	public void setPcode(int pcode){
			this.pcode=pcode; 
	}

	public String getEnCity(){
			return enCity; 
	}
	public void setEnCity(String enCity){
			this.enCity=enCity; 
	}

}